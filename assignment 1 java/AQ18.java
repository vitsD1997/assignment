/*

Write a program to find minimum and maximum number as well as
to add two integer numbers using methods of Integer.

*/

class AQ18
{
    public static void main(String args[])
    {
        int a=99;
        int b=9;
        System.out.println("Maximum value is= "+Integer.max(a,b));
        System.out.println("Minimum value is= "+Integer.min(a,b));
        System.out.println("Addition is= "+Integer.sum(a,b));
    }
}